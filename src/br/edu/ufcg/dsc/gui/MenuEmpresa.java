private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
	String name = nome.getText();
	String year = anoFundacao.getText();
	Cidade city = (Cidade)cidade.getSelectedValue();
	Empresa empr = new Empresa(0,name, Integer.parseInt(year), city.getIdentificacao());
	try {
		facade.adicionarEmpresa(empr);
		JOptionPane.showMessageDialog(null, "A empresa foi adicionada com sucesso!");
	} catch (SQLException e) {
		 JOptionPane.showMessageDialog(null, e.getMessage(),
	             "Nao foi possivel remover a empresa",
	             JOptionPane.ERROR_MESSAGE);
	}

	MainMenuEmpresa m = new MainMenuEmpresa();
	m.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
	m.setVisible(true);
	dispose();
}//GEN-LAST:event_cadastrarActionPerformed

